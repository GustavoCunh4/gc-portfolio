export const getInitialLocale = () => {
  const saved = localStorage.getItem('gc-locale');
  if (saved && translations[saved]) return saved;
  return navigator.language?.toLowerCase().startsWith('pt') ? 'pt-BR' : 'en';
};

export const getMessage = (locale, key) => {
  return key.split('.').reduce((value, segment) => value?.[segment], translations[locale]) ?? key;
};

export function applyTranslations(locale) {
  document.documentElement.lang = locale;

  document.querySelectorAll('[data-i18n]').forEach(element => {
    element.textContent = getMessage(locale, element.dataset.i18n);
  });

  document.querySelectorAll('[data-i18n-aria-label]').forEach(element => {
    element.setAttribute('aria-label', getMessage(locale, element.dataset.i18nAriaLabel));
  });

  const { meta } = translations[locale];
  document.querySelector('meta[name="description"]')?.setAttribute('content', meta.description);
  document.querySelector('meta[property="og:description"]')?.setAttribute('content', meta.socialDescription);
  document.querySelector('meta[name="twitter:description"]')?.setAttribute('content', meta.socialDescription);
}
